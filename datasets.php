         </div>  
         <h3>World Bank IBRD-IDA, Level 1, Version 1.0</h3>
         <div>
            <p class="description">This is the 1.0 version  of the Level 1 product, of all projects approved from 2000-2011 of the World Bank IBRD/IDA lending lines. 3534 projects with  41307 locations have been sub-nationally geocoded, representing 99% of total commitments. Disbursement data was not geocoded. </p>
