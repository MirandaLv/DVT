      <div id="donor" class="tab">
         <h3>Brazil's South-South Cooperation, 2005-2011</h3>
         <div>
            <p class="description">Brazil's South-South cooperation projects from 2011 have not been integrated into AidData's database, but they are available here for download, along with records from 2005 to 2010. These records were provided by Agência Brasileira de Cooperação. Note that the most recent table is titled "In execution," so these projects may have been reported in earlier years as well.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_SXUzUXoxSHFJWmc/edit?usp=sharing">Download</a></p>
         </div>
         <h3>DFATD - Canadian Department of Foreign Affairs, Trade, and Development, webscraped data, March 2009</h3>
         <div>
            <p class="description">This dataset of 2,740 projects draws information from a public DFATD database as of March 2009. For information on current projects, please visit the DFATD database. The dataset includes financial information, information on executing agencies, sectors, and project abstracts, as well as links to the original source for each project.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_ZGtScjMxZjQyNXM/edit?usp=sharing">Download</a></p>
         </div>
         <h3>Chinese Foreign Aid</h3>
         <div>
            <p class="description">This dataset represents only a fraction of the total aid given by China. To learn more about AidData’s Methodology for Tracking Underreported Financial Flows and access comprehensive data on Chinese development finance flows to Africa, visit http://china.aiddata.org/.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_Q1N2QW1jcmtQdXM/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Delegated Cooperation, Humanitarian Relief and Scholarships of Cyprus, 2006-2010</h3>
         <div>
            <p class="description">This dataset is comprised of donor-provided financial information complemented by descriptive information from the public domain for 27 cooperation projects funded or co-funded by the government of Cyprus. It contains a list of scholarships provided by the Development Cooperation of Cyprus (without financial information).</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_cUFIeDdfOUV1ZjA/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Czech Republic's International Development Cooperation Activities, 1996-2009</h3>
         <div>
            <p class="description">This dataset, provided by the Czech Development Agency (CzDA) is available here for download. These 1,274 records have financial and descriptive information, as well as unique project IDs. They have  been translated, screened and coded by AidData staff, and can be queried through AidData's advanced aid search.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_YVR5NmVMTWduMGs/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>European Bank for Reconstruction and Development, webscraped data, Fall 2009</h3>
         <div>
            <p class="description">This dataset of 1,513 projects draws information from a public EBRD database as of December 2009. For information on current projects, please visit the EBRD database. The dataset includes financial information, environmental impact, and other project information as well as links to the original source for each project.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_T2xfTDFxNHdxQkk/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Israel's Technical Cooperation, 2002-2009</h3>
         <div>
            <p class="description">This document contains descriptive information for 25 technical cooperation projects by Israel's Agency for International Development Cooperation (MASHAV), which are also included in the AidData database. It is offered here as provided by MASHAV. Financial information was not provided for these projects.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_d2dQYloxWnlDc0U/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Korea International Cooperation Agency (KOICA), webscraped data, 1991-2007</h3>
         <div>
            <p class="description">Information for this dataset was pulled from a publicly available KOICA database in February of 2009. It includes 29,549 KOICA projects covering 1991-2007 and totaling nearly $1.5 billion in commitments. AidData sector and activity codes have been applied.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_eE9OYUFJVHROUUE/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Liechtenstein's Cooperation Projects, 1981-2008</h3>
         <div>
            <p class="description">These 895 records were gathered from the annual reports of the Liechtenstein Development Service (Liechtensteinischer Entwicklungsdienst, LED). The financial information is very thorough. This dataset has AidData sector and activity codes,  formatted and translated, and can be accessed through AidData's advanced aid search.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_d1F4eTh3QlhMeGs/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Lithuanian Development Cooperation, 2009</h3>
         <div>
            <p class="description">These project records were gathered from the 2009 project list released by the Development Cooperation and Democracy Promotion Department of the Ministry of Foreign Affairs. They are provided with Lithuanian descriptive information, automatically translated English descriptive information and commitment amounts in Lithuanian Litas.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_Y3dEVlZrSHBiU2s/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>Taiwan's Lending and Investment projects, 1991-2011</h3>
         <div>
            <p class="description">Provided by the Taiwan International Cooperation and Development Fund (TaiwanICDF), this table provides detailed descriptive and financial information about Taiwan's development-oriented lending and investment projects since 1991. These 68 projects have been integrated into AidData, and have received AidData sector and activity codes.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_YlRxNlFfRXdQcVU/edit?usp=sharing">Download</a></p>
         </div> 
         <h3>US Agency for International Development (USAID), webscraped data, 1946-1996</h3>
         <div>
            <p class="description">USAID projects draw information from a public USAID database and were web-scraped and audited by AidData to ensure the most complete data possible. The dataset includes expanded project descriptions. AidData sector and activity codes have also been applied. The “Past Projects Database” is a legacy database of project descriptions which was replaced by the current USAID DEC system in the 1990s when USAID restructured activities to no longer include the concept of a “project.” Funding amounts are not included in this dataset, though AidData is working with USAID to release those details in the future.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_d2MwejI3WFU3ZjQ/edit?usp=sharing">Download</a></p>
         </div>       
