         <h3>Malawi Aid Management Platform</h3>
         <div>
            <p class="description">This dataset contains geocoded aid locations from 30 donor agencies in the Malawi Ministry of Finance’s Aid Management Platform. The data include US$5.3 billion in commitments, representing approximately 80% of all external funding reported to the Malawi Ministry of Finance since 2000.</p>
            <p class="link">Link: <a href="https://drive.google.com/file/d/0B1I9MfL0ynr_RVA1SHg0dGJaS0E/edit?usp=sharing">Download</a></p>
         </div>
         <h3>Timor-Leste Aid Management Platform, Level 1, Version 1.0</h3>
         <div>
            <p class="description">This is the 1.0 version  of the Level 1 product, of all projects from the Aid Management Platform (AMP) managed for Timor-Leste, of which 907 projects have been sub-nationally geocoded, representing 76% of total commitments and 79% of total disbursements. This dataset includes projects from 79 donors with 3565 locations reported between 1995 and 2015.</p>
            <p class="link">Link: <a href="https://data.itpir.wm.edu/aiddatascratch/aiddata/geocoded/timor-leste_geocoded_v_1.0.zip">Download</a></p>
         </div>  
         <h3>Senegal Aid Management Platform, Level 1, Version 1.0</h3>
         <div>
            <p class="description">This is the 1.0 version  of the Level 1 product, of all projects from the Aid Management Platform (AMP) managed for Senegal, of which 406 projects have been sub-nationally geocoded, representing 38% of total commitments and 46% of total disbursements. This dataset includes projects from 79 donors with 1100  locations reported between 2000 and 2012. </p>
            <p class="link">Link: <a href="https://data.itpir.wm.edu/aiddatascratch/aiddata/geocoded/senegal_geocoded_v_1.0.zip">Download</a></p>
         </div>  
         <h3>Nepal Aid Management Platform, Version 1.0</h3>
         <div>
            <p class="description">This is the 1.0 version of all projects from the Aid Management Platform (AMP) managed by Nepal’s Ministry of Finance, of which 482 projects have been sub-nationally geocoded, representing 73% of total commitments and 81% of total disbursements. This dataset includes projects from 108 donors with 21,000 locations reported between 2010 and 2012. Boundary shapefiles for each administrative level in Nepal are included from a World Bank source.</p>
            <p class="link">Link: <a href="https://docs.google.com/a/developmentgateway.org/file/d/0B1I9MfL0ynr_ckdOM2VCUlFkQTQ/edit">Download</a></p>
         </div>  
