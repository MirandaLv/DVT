         <h3>AidData’s Chinese Official Finance to Africa Dataset, Version 1.1.1</h3>
         <div>
            <p class="description">This is a sub-nationally georeferenced dataset of Chinese official finance activities that spans 50 African countries over the 2000-2012 period. It includes 1,955 projects and more than 3,545 project locations. In order to build this dataset, AidData applied its double-blind geocoding methodology to the 1.1 (static research release) version of its Chinese Official Finance to Africa Dataset.</p>
